last_checked_id = None

def send_telegram(text):
    if not TELEGRAM_TOKEN or not TELEGRAM_CHAT_ID:
        return False
        
    try:
        url = f"https://api.telegram.org/bot{TELEGRAM_TOKEN}/sendMessage"
        data = {"chat_id": TELEGRAM_CHAT_ID, "text": text}
        response = requests.post(url, data=data, timeout=10)
        
        if response.status_code == 200:
            logger.info(f"üì± –û—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ: {text}")
            return True
        else:
            return False
    except:
        return False

def get_full_message_text(message):
    """–ò–∑–≤–ª–µ–∫–∞–µ—Ç –í–ï–°–¨ —Ç–µ–∫—Å—Ç –∏–∑ —Å–æ–æ–±—â–µ–Ω–∏—è"""
    full_text = message.get('content', '')
    
    embeds = message.get('embeds', [])
    for embed in embeds:
        full_text += f" {embed.get('title', '')}"
        full_text += f" {embed.get('description', '')}"
        full_text += f" {embed.get('footer', {}).get('text', '')}"
        
        for field in embed.get('fields', []):
            full_text += f" {field.get('name', '')}"
            full_text += f" {field.get('value', '')}"
    
    return full_text

def check_discord_messages():
    global last_checked_id
    
    try:
        url = f"https://discord.com/api/v10/channels/{DISCORD_CHANNEL_ID}/messages?limit=5"
        headers = {"Authorization": f"Bot {DISCORD_TOKEN}"}
        
        response = requests.get(url, headers=headers, timeout=10)
        
        if response.status_code == 200:
            messages = response.json()
            
            # –°–æ—Ä—Ç–∏—Ä—É–µ–º –æ—Ç –Ω–æ–≤–æ–≥–æ –∫ —Å—Ç–∞—Ä–æ–º—É
            messages.sort(key=lambda x: x['id'], reverse=True)
            
            found_tomato = False
            
            for message in messages:
                message_id = message['id']
                author = message.get('author', {}).get('username', 'Unknown')
                
                # –¢–æ–ª—å–∫–æ —Å–æ–æ–±—â–µ–Ω–∏—è –í—É–ª–∫–∞–Ω–∞
                if 'Vulcan' not in author:
                    continue
                
                # –ï—Å–ª–∏ –¥–æ—à–ª–∏ –¥–æ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –ø—Ä–æ–≤–µ—Ä–µ–Ω–Ω–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è - –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º—Å—è
                if last_checked_id and message_id <= last_checked_id:
                    break
                
                full_text = get_full_message_text(message)
                logger.info(f"üîç –ü—Ä–æ–≤–µ—Ä—è—é –Ω–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ: {full_text[:80]}...")
                
                # –ò—â–µ–º –¢–û–ú–ê–¢ –≤ –Ω–æ–≤–æ–º —Å–æ–æ–±—â–µ–Ω–∏–∏
                if 'Tomato' in full_text or 'To...' in full_text:
                    logger.info("üéØ –û–ë–ù–ê–†–£–ñ–ï–ù –¢–û–ú–ê–¢ –í –ù–û–í–û–ú –°–û–û–ë–©–ï–ù–ò–ò!")
                    send_telegram("üçÖ –¢–æ–º–∞—Ç –≤ —Å—Ç–æ–∫–µ!")
                    found_tomato = True
                    break  # –ù–∞—à–ª–∏ —Ç–æ–º–∞—Ç - –≤—ã—Ö–æ–¥–∏–º
            
            # –û–±–Ω–æ–≤–ª—è–µ–º ID –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –ø—Ä–æ–≤–µ—Ä–µ–Ω–Ω–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è
            if messages:
                last_checked_id = messages[0]['id']  # ID —Å–∞–º–æ–≥–æ –Ω–æ–≤–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è
            
            return found_tomato
        else:
            return False
            
    except Exception as e:
        logger.error(f"üí• –û—à–∏–±–∫–∞: {e}")
        return False

